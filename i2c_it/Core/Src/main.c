/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  HAL_StatusTypeDef status;
  uint8_t i2c_buf[16];
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the System Power */
  SystemPower_Config();

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ICACHE_Init();
  MX_USART1_UART_Init();
  MX_I2C1_Init();
  /* USER CODE BEGIN 2 */
  printf("\r******* Start *******\r\n");

  i2c_buf[0] = TMP102_REG_CFG;
  i2c_buf[1] = 0x60; // Config byte 1: default value
  i2c_buf[2] = 0x60; // Config byte 2: conversion rate: 1 Hz
  status = HAL_I2C_Master_Transmit_IT(&hi2c1, TMP102_I2C_ADDR, i2c_buf, 3U);
  if (status == HAL_OK) {
	  while (i2c_tx_cplt == 0U) {} // Wait for completion
	  i2c_tx_cplt = 0U;
  } else {
	  printf("Error at I2C transmit (config register)\r\n");
  }
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  if (i2c_tx_cplt) {
		  i2c_tx_cplt = 0U;
		  status = HAL_I2C_Master_Receive_IT(&hi2c1, TMP102_I2C_ADDR, i2c_buf, 2U);
		  if (status != HAL_OK) {
			  printf("Error at I2C receive\r\n");
		  }
	  }

	  if (i2c_rx_cplt) {
		  i2c_rx_cplt = 0U;
		  uint16_t temp_uint = ((uint16_t)i2c_buf[0]) << 4U; // Byte 1: bits 11 .. 4
		  temp_uint |= ((uint16_t)i2c_buf[1]) >> 4U; 		// Byte 2: bits 3 .. 0
		  if ((temp_uint & 0x800) > 0U) { // If sign bit is set, propagate it to 4 hsbs
		  	temp_uint |= 0xF000;
		  }
		  float temp_C = (float)((int16_t)temp_uint) * 0.0625f; // Conversion to signed and multiplication by increment
		  printf("temperature: %ld degC\r\n", (int32_t)temp_C);
	  }

	  if (button_rising) {
		  button_rising = 0U;
		  HAL_GPIO_WritePin(LED_RED_GPIO_Port, LED_RED_Pin, GPIO_PIN_SET);

		  // Transmit
		  i2c_buf[0] = TMP102_REG_TEMP;
		  status = HAL_I2C_Master_Transmit_IT(&hi2c1, TMP102_I2C_ADDR, i2c_buf, 1U);
		  if (status != HAL_OK) {
			  printf("Error at I2C transmit\r\n");
		  }
	  }

	  if (button_falling) {
		  button_falling = 0U;
		  HAL_GPIO_WritePin(LED_RED_GPIO_Port, LED_RED_Pin, GPIO_PIN_RESET);
	  }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

